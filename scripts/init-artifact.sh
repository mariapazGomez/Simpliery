  * {
    @apply border-border;
  }
  body {
    @apply bg-background text-foreground;
  }
}
EOF

# ── 10. Initialize shadcn/ui (components.json) ────────────────────────────
cat > components.json << 'EOF'
{
  "$schema": "https://ui.shadcn.com/schema.json",
  "style": "default",
  "rsc": false,
  "tsx": true,
  "tailwind": {
    "config": "tailwind.config.js",
    "css": "src/index.css",
    "baseColor": "slate",
    "cssVariables": true,
    "prefix": ""
  },
  "aliases": {
    "components": "@/components",
    "utils": "@/lib/utils",
    "ui": "@/components/ui",
    "lib": "@/lib",
    "hooks": "@/hooks"
  }
}
EOF

echo ""
echo "✅ Project '$PROJECT_NAME' initialized!"
echo ""
echo "Next steps:"
echo "  cd $PROJECT_NAME"
echo "  # Edit src/App.tsx to build your artifact"
echo "  # Add shadcn/ui components: npx shadcn@latest add button card ..."
echo "  # Bundle when ready: bash ../scripts/bundle-artifact.sh"
